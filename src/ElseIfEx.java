
public class ElseIfEx 
{
public static void main(String args[])
{ 
int x = 32; 

if( x < 10 )
{ System.out.print("Value of X is Less Then 10"); }
else if( x < 20 )
{ System.out.print("Value of X is Less Then 20"); }
else if( x < 31)
{ System.out.print("Value of X is Less Then 35"); }

else{ System.out.print("This is else statement"); } 
}

}